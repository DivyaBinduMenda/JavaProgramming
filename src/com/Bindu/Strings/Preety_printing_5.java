package com.Bindu.Strings;

public class Preety_printing_5 {
	
	public static void main(String[] args) {
		
		float a=345.7667f;
		
		//Here % is placeholder
		System.out.printf("Formatted number is %.2f ", a); //print the 2 decimals after .
		System.out.println();
		System.out.println(Math.PI);
		System.out.println();
		System.out.printf("Pie value is %.3f ",Math.PI); //PRINT THE 3 DECIMALS AFTER .
		System.out.println();
		System.out.printf("Hi my name is %s  and  I am very %s", "Bindu", "Cool");
	}

}
